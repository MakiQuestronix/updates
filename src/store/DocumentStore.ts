import { create } from "zustand";

export type ProcessingStatus =
  | "Completed"
  | "Processing"
  | "Pending"
  | "Failed";
export type ApprovalStatus = "Approved" | "Pending" | "Decline";
export type DocStatus = "Active" | "Archived";

export type PipelineStage = {
  step: number;
  name: string;
  status: ProcessingStatus;
  completedAt?: string;
};

export type ApprovalHistoryEvent = {
  id: string;
  action: string;
  actorName: string;
  actorRole: string;
  timestamp: string;
  isApproved: boolean;
};

export type ApprovalInfo = {
  status: ApprovalStatus;
  approvedBy?: {
    name: string;
    role: string;
    avatarUrl?: string;
  };
  approvedOn?: string;
  comments?: string;
  history: ApprovalHistoryEvent[];
};

export type OCRImage = {
  id: string;
  thumbnailUrl: string;
  label?: string;
};

export type DocumentDetail = {
  id: string;
  knowledgeBaseId: string;
  knowledgeBaseName: string;
  name: string;
  type: string;
  size: string;
  uploadedAt: string;
  uploadedBy: string;
  pagesOCR: number;
  imagesExtracted: number;
  wordsExtracted: number;
  processingTime: string;
  owner: string;
  ownerAvatarUrl?: string;
  dateAdded: string;
  lastEditor: string;
  lastEditorAvatarUrl?: string;
  lastModified: string;
  uploadPath: string;
  pipelineStatus: "All Stages Complete" | "In Progress" | "Failed";
  pipelineStages: PipelineStage[];
  pipelineMessage?: string;
  markdownUrl?: string;
  markdownDownloadUrl?: string;
  ocrImages: OCRImage[];
  status: ProcessingStatus;
  approval: ApprovalStatus;
  docStatus: DocStatus;
  archivedBy?: string;
  archivedDate?: string;
  approvalInfo: ApprovalInfo;
  activityLog: {
    id: string;
    action: string;
    actorName: string;
    actorRole: string | null;
    actorAvatarUrl: string | null;
    timestamp: string;
  }[];
  metadata?: {
    processingMetadata: Record<string, string>;
    customMetadata: Record<string, string>;
  };
};

export type DocumentStore = {
  currentDocument: DocumentDetail | null;
  isLoading: boolean;
  fetchDocumentById: (id: string) => Promise<void>;
};

